package com.movie.moviesite.entity;

public class Stu {

    private int stuId;
    private String stuName;

    public Stu(){}

    public int getStuId() {
        return stuId;
    }

    public void setStuId(int stuId) {
        this.stuId = stuId;
    }

    public String getStuName() {
        return stuName;
    }

    public void setStuName(String stuName) {
        this.stuName = stuName;
    }
}
